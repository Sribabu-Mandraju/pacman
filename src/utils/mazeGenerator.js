export const generateMaze = (level) => {
  const mazeTemplate = PACMAN_MAZES[level] || PACMAN_MAZES[1];
  const maze = mazeTemplate.map((row) => [...row]);

  const rows = maze.length;
  const cols = maze[0].length;

  const spawnY = rows - 2;
  const spawnX = Math.floor(cols / 2);

  for (let dy = 0; dy < 6; dy++) {
    const y = spawnY - dy;
    if (y >= 0 && y < rows) {
      for (let dx = -2; dx <= 2; dx++) {
        const x = spawnX + dx;
        if (x >= 0 && x < cols) {
          maze[y][x] = 0;
        }
      }
    }
  }

  return {
    pattern: maze,
    spawnPoint: { x: spawnX, y: spawnY },
    dimensions: { rows, cols },
    noSpawnArea: {
      centerX: spawnX,
      centerY: spawnY,
      radius: 5,
    },
  };
};

